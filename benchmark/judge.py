"""LLM-as-Judge: Score model outputs using Claude Opus on native Bedrock."""
import json
import csv
import sys
import os
from pathlib import Path

try:
    import boto3
except ImportError:
    print("[error] boto3 required: pip install boto3")
    sys.exit(1)

JUDGE_MODEL = "us.anthropic.claude-opus-4-6-v1"
REGION = "us-east-1"

RUBRIC = """You are evaluating code generated by a coding agent. Score each dimension 1-5.

## Scoring Rubric

**Correctness (1-5):**
1 = Does not compile/run, completely wrong approach
2 = Partially works but has bugs
3 = Works for basic cases, misses edge cases
4 = Fully correct, handles edge cases
5 = Perfect, handles all cases elegantly

**Code Quality (1-5):**
1 = Unreadable, no structure
2 = Works but messy, poor naming
3 = Acceptable, standard quality
4 = Clean, well-structured, good naming
5 = Exemplary, production-ready, idiomatic

**Completeness (1-5):**
1 = Barely addresses the prompt
2 = Partial solution, missing major requirements
3 = Addresses most requirements
4 = Fully addresses all requirements
5 = Exceeds requirements with thoughtful additions

**Efficiency (1-5):**
1 = Extremely inefficient, wasteful
2 = Unnecessary complexity or redundancy
3 = Acceptable performance
4 = Well-optimized for the use case
5 = Optimal solution

## Response Format
Return ONLY valid JSON:
{"correctness": N, "quality": N, "completeness": N, "efficiency": N, "notes": "one sentence summary"}
"""


def judge_output(client, task_name: str, prompt: str, output: str) -> dict:
    """Score a single model output."""
    message = f"""## Task
{task_name}

## Original Prompt
{prompt}

## Model Output (code changes and reasoning)
{output[:8000]}

{RUBRIC}"""

    response = client.invoke_model(
        modelId=JUDGE_MODEL,
        contentType="application/json",
        accept="application/json",
        body=json.dumps({
            "anthropic_version": "bedrock-2023-05-31",
            "max_tokens": 500,
            "messages": [{"role": "user", "content": message}],
        }),
    )

    result = json.loads(response["body"].read())
    text = result["content"][0]["text"]

    try:
        start = text.index("{")
        end = text.rindex("}") + 1
        scores = json.loads(text[start:end])
        return scores
    except (ValueError, json.JSONDecodeError):
        return {"correctness": 0, "quality": 0, "completeness": 0, "efficiency": 0, "notes": "parse error"}


def main():
    if len(sys.argv) < 3:
        print("Usage: python judge.py <results.csv> <outputs_dir>")
        sys.exit(1)

    results_csv = sys.argv[1]
    outputs_dir = sys.argv[2]
    tasks_dir = Path(__file__).parent / "tasks"

    client = boto3.client("bedrock-runtime", region_name=REGION)

    # Read results to know which model/task combos to judge
    judge_results = []
    with open(results_csv) as f:
        reader = csv.DictReader(f)
        for row in reader:
            model = row["model"]
            task = row["task"]
            output_file = Path(outputs_dir) / model / f"{task}.txt"
            prompt_file = tasks_dir / task / "prompt.txt"

            if not output_file.exists():
                print(f"  [skip] {model}/{task} — no output file")
                continue

            prompt = prompt_file.read_text() if prompt_file.exists() else task
            output = output_file.read_text()

            print(f"  [judge] {model}/{task} ... ", end="", flush=True)
            scores = judge_output(client, task, prompt, output)
            scores["model"] = model
            scores["task"] = task
            judge_results.append(scores)

            avg = sum(scores.get(k, 0) for k in ["correctness", "quality", "completeness", "efficiency"]) / 4
            print(f"{avg:.1f}/5 ({scores.get('notes', '')})")

    # Write judge results
    judge_csv = results_csv.replace(".csv", "_judge.csv")
    if judge_results:
        with open(judge_csv, "w", newline="") as f:
            writer = csv.DictWriter(f, fieldnames=["model", "task", "correctness", "quality", "completeness", "efficiency", "notes"])
            writer.writeheader()
            writer.writerows(judge_results)
        print(f"\n  Judge scores saved to: {judge_csv}")

        # Print summary
        print("\n  Quality Scores (avg across tasks):")
        print(f"  {'Model':<20} {'Correct':>8} {'Quality':>8} {'Complete':>8} {'Efficient':>8} {'Avg':>6}")
        models = sorted(set(r["model"] for r in judge_results))
        for model in models:
            model_scores = [r for r in judge_results if r["model"] == model]
            avg_c = sum(r.get("correctness", 0) for r in model_scores) / len(model_scores)
            avg_q = sum(r.get("quality", 0) for r in model_scores) / len(model_scores)
            avg_comp = sum(r.get("completeness", 0) for r in model_scores) / len(model_scores)
            avg_e = sum(r.get("efficiency", 0) for r in model_scores) / len(model_scores)
            avg_all = (avg_c + avg_q + avg_comp + avg_e) / 4
            print(f"  {model:<20} {avg_c:>8.1f} {avg_q:>8.1f} {avg_comp:>8.1f} {avg_e:>8.1f} {avg_all:>6.1f}")


if __name__ == "__main__":
    main()
